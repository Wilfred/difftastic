static bool is_sp(uint32_t c) {
	return (
		c == ' ' ||
		c == '\t' ||
		c == 0x20 ||
		c == 0x1680 ||
		(c >= 0x2000 && c <= 0x200a) ||
		c == 0x202f ||
		c == 0x205f ||
		c == 0x3000
	);
}
static bool is_lb(uint32_t c) {
	return (
		c == '\n' ||
		c == '\r' ||
		c == '\v' ||
		c == '\f' ||
		c == 0x85 ||
		c == 0x2028 ||
		c == 0x2029
	);
}

